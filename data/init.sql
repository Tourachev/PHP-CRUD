CREATE DATABASE games;

use games;

CREATE TABLE playerProfile (
	playerId INT(11) UNSIGNED AUTO_INCREMENT PRIMARY KEY, 
	username VARCHAR(30) NOT NULL
);

CREATE TABLE game (
	gameId INT(11) UNSIGNED AUTO_INCREMENT PRIMARY KEY, 
	description VARCHAR(30) NOT NULL
);

CREATE TABLE server (
	serverId INT(11) UNSIGNED AUTO_INCREMENT PRIMARY KEY, 
	servername VARCHAR(30) NOT NULL
);

CREATE TABLE log (
	gameId INT(11)UNSIGNED AUTO_INCREMENT PRIMARY KEY,
	playerId INT(11),  
	serverId INT(11),
	date VARCHAR(11)
);